// 使用位运算求解x的算术平方根
int mySqrt(int x)
{
    // m初始化为2^30，用于逐位检查
    unsigned m = 0x40000000, y = 0, b = 0;

    while (m != 0)
    {
        // 计算当前位可能的平方值
        b = y | m;
        // y右移一位，为下一位做准备
        y = y >> 1;
        // 如果x大于等于当前平方值，说明该位可以取1
        if (x >= b)
        {
            // 更新x的值
            x = x - b;
            // 将当前位设为1
            y = y | m;
        }
        // m右移两位，检查下一位
        m = m >> 2;
    }

    return y;
}